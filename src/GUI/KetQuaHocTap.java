    private void btnExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportActionPerformed

        XSSFWorkbook wb = new XSSFWorkbook();
        XSSFSheet sheet = wb.createSheet("Result");
        sheet.setColumnWidth(0, 4000);
        sheet.setColumnWidth(1, 10000);
        sheet.setColumnWidth(2, 6000);
        sheet.setColumnWidth(3, 6000);
        sheet.setColumnWidth(4, 6000);
        sheet.setColumnWidth(5, 6000);

        int rownum = 2;
        Row row;
        Cell cell;
        XSSFCellStyle style = createStyleForTitle(wb);
        XSSFCellStyle styleBold = createStyleForBold(wb);
        XSSFCellStyle styleData = createStyleForData(wb);
        row = sheet.createRow(rownum);

        
        XSSFRow title = sheet.createRow(0);
        Cell titleCell = title.createCell(2);
        String hocky=cb_hockykqht.getSelectedItem().toString().toUpperCase();
     
        titleCell.setCellValue("KẾT QUẢ HỌC TẬP " + hocky + " NĂM HỌC " + cb_namhockqht.getSelectedItem());
        
        titleCell.setCellStyle(style);

        // Số TT
        cell = row.createCell(0, CellType.STRING);
        cell.setCellValue("Số TT");
        cell.setCellStyle(styleBold);
        // Môn học
        cell = row.createCell(1, CellType.STRING);
        cell.setCellValue("Môn học");
        cell.setCellStyle(styleBold);
        // Điểm quá trình
        cell = row.createCell(2, CellType.STRING);
        cell.setCellValue("Điểm quá trình");
        cell.setCellStyle(styleBold);
        // Điểm giữa kỳ
        cell = row.createCell(3, CellType.STRING);
        cell.setCellValue("Điểm giữa kỳ");
        cell.setCellStyle(styleBold);
        // Điểm cuối kỳ
        cell = row.createCell(4, CellType.STRING);
        cell.setCellValue("Điểm cuối kỳ");
        cell.setCellStyle(styleBold);
        // Điểm trung bình
        cell = row.createCell(5, CellType.STRING);
        cell.setCellValue("Điểm trung bình");
        cell.setCellStyle(styleBold);

        for (int i = 0; i < bangkqht.getRowCount(); i++) {
            rownum++;
            row = sheet.createRow(rownum);
            // Số TT
            cell = row.createCell(0, CellType.NUMERIC);
            cell.setCellValue(i + 1);
            cell.setCellStyle(styleData);

            // Môn học 
            cell = row.createCell(1, CellType.STRING);
            cell.setCellValue((String) bangkqht.getValueAt(i, 1));
            cell.setCellStyle(styleData);

            // Điểm quá trình 
            cell = row.createCell(2, CellType.NUMERIC);
            cell.setCellValue(String.valueOf(bangkqht.getValueAt(i, 2)));
            cell.setCellStyle(styleData);
            // Điểm giữa kỳ 
            cell = row.createCell(3, CellType.NUMERIC);
            cell.setCellValue(String.valueOf(bangkqht.getValueAt(i, 3)));
            cell.setCellStyle(styleData);
            // Điểm cuối kỳ
            cell = row.createCell(4, CellType.NUMERIC);
            cell.setCellValue(String.valueOf(bangkqht.getValueAt(i, 4)));
            cell.setCellStyle(styleData);
            // Điểm trung bình
            cell = row.createCell(5, CellType.NUMERIC);
            cell.setCellValue(String.valueOf(bangkqht.getValueAt(i, 5)));
            cell.setCellStyle(styleData);
        }
        XSSFRow sum = sheet.createRow(10);
        Cell sumLabelCell = sum.createCell(3);
        sumLabelCell.setCellValue("Điểm trung bình học kỳ :");
        sumLabelCell.setCellStyle(styleBold);
        Cell sumCell = sum.createCell(4);
        sumCell.setCellType(CellType.FORMULA);
        sumCell.setCellFormula(txtdiemtb.getText());
        sumCell.setCellStyle(styleData);

        File file = new File("E:/Learn Java/Slide UIT/" + Main.nameLogin + ".xlsx");
        file.getParentFile().mkdirs();
        
        try {
            FileOutputStream outFile = new FileOutputStream(file);
            wb.write(outFile);
            Desktop desktop = Desktop.getDesktop();
            desktop.open(file);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(KetQuaHocTap.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(KetQuaHocTap.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnExportActionPerformed

