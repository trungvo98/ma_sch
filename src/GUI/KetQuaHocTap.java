    public void loadKetquahoctap() {
        model.setRowCount(0);
        listDiem = new DiemDAO().findByMaSV(Main.nameLogin, cb_hockykqht.getSelectedItem().toString(), cb_namhockqht.getSelectedItem().toString());
        int i = 0;
        for (Diem d : listDiem) {
            i++;
            model.addRow(toObjectsData(d, i)); // Thêm vào 1 dòng cuối cùng trong bảng Kết quả học tập 
        }
        bangkqht.setModel(model);

        float result = new DiemDAO().calDiemTB(Main.nameLogin, cb_hockykqht.getSelectedItem().toString(), cb_namhockqht.getSelectedItem().toString());
        String str = Float.toString(result);
        txtdiemtb.setText(str);

    }

    public Object[] toObjectsData(Diem d, int i) {

        lhp = new LopHocPhanDAO().findByMaLHP(d.getMaLHP(), d.getHocky(), d.getNamhoc());

        MonHoc mh = new MonHocDAO().findByMaMH(lhp.getMaMH());

        Object[] objectsData = {i, mh.getTenMH(), d.getDiemQT(), d.getDiemGK(), d.getDiemCK(), d.getDiemTB()};

        return objectsData;

    }

    private static XSSFCellStyle createStyleForTitle(XSSFWorkbook workbook) {
        XSSFFont font = workbook.createFont();
        font.setBold(true);
        font.setFontHeightInPoints((short) 16);
        
        XSSFCellStyle style = workbook.createCellStyle();
        style.setVerticalAlignment(VerticalAlignment.CENTER);
        style.setFont(font);
        return style;
    }

    private static XSSFCellStyle createStyleForBold(XSSFWorkbook workbook) {
        XSSFFont font = workbook.createFont();
        font.setBold(true);

        XSSFCellStyle style = workbook.createCellStyle();
        style.setVerticalAlignment(VerticalAlignment.CENTER);
        style.setAlignment(HorizontalAlignment.LEFT);
        style.setFont(font);
        return style;
    }

    private static XSSFCellStyle createStyleForData(XSSFWorkbook workbook) {
        XSSFFont font = workbook.createFont();
        XSSFCellStyle style = workbook.createCellStyle();
        style.setVerticalAlignment(VerticalAlignment.CENTER);
        style.setAlignment(HorizontalAlignment.LEFT);
        style.setFont(font);
        return style;
    }

