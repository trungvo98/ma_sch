    private void btnXoaLHPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaLHPActionPerformed
        String maLHP = (String) bangdslhp.getValueAt(bangdslhp.getSelectedRow(), 0);
        String hocky = cbhockylhp.getSelectedItem().toString();
        String namhoc = cbnamhoclhp.getSelectedItem().toString();

        int check = JOptionPane.showConfirmDialog(this, "Nếu bạn xóa lớp học phần này thì tất cả sinh viên đã đăng ký\n học phần này sẽ bị xóa hết"
                + "và những thứ liên quan đến lớp học phần này cũng bị hủy\nBạn có chắc chắn muốn xóa không?", "Cảnh báo", JOptionPane.YES_NO_OPTION);
        if (check == JOptionPane.YES_OPTION) {
            new DangKyHocDAO().deleteByMaLHP(maLHP, hocky, namhoc);
            LichThi lt = new LichThi(maLHP, hocky, namhoc);
            new LichThiDAO().xoaLichthi(lt);
            LopHocPhan lhp = new LopHocPhan(maLHP, hocky, namhoc);
            new LopHocPhanDAO().xoaLHP(lhp);
            JOptionPane.showMessageDialog(this, "Bạn đã xóa lớp học phần thành công", "Thông báo ", JOptionPane.INFORMATION_MESSAGE);
            loaddslhp();
            resetFormQllhp();
        }
    }//GEN-LAST:event_btnXoaLHPActionPerformed

