    private void btnThemLHPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemLHPActionPerformed
        if (!checkinfoLHP()) {
            return;
        }
        String malhp = txtmalhp.getText();
        String hocky = cbhocky.getSelectedItem().toString();
        String namhoc = cbnamhoc.getSelectedItem().toString();
        String mamh = cbmamh.getSelectedItem().toString();
        String thu = txtthu.getText();
        String tiet = txttiet.getText();
        String phong = txtphong.getText();
        String magv = cbmagv.getSelectedItem().toString();
        String siso = txtsiso.getText();
        String ngbd = new DayNow().converttoString(new DayNow().convertFromJAVADateToSQLDate(dcngbd.getDate()));
        String ngkt = new DayNow().converttoString(new DayNow().convertFromJAVADateToSQLDate(dcngkt.getDate()));
        LopHocPhan lhp = new LopHocPhan(malhp, mamh, thu, tiet, phong, siso, magv, ngbd, ngkt, hocky, namhoc);
        boolean chmalhp = new LopHocPhanDAO().checkMaLHP(malhp, hocky, namhoc);
        if (!chmalhp) {
            JOptionPane.showMessageDialog(this, "Lớp học phần này đã tồn tại\nXin vui lòng thử lại.", "Thông báo", JOptionPane.ERROR_MESSAGE);
        } else {
            if (magv.equals("")) {
                boolean insert = new LopHocPhanDAO().themLHPnoGV(lhp);
                if (insert) {
                    JOptionPane.showMessageDialog(this, "Thêm lớp học phần thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    loaddslhp();
                    resetFormQllhp();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm lớp học phần thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                boolean insert = new LopHocPhanDAO().themLHP(lhp);
                if (insert) {
                    JOptionPane.showMessageDialog(this, "Thêm lớp học phần thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    loaddslhp();
                    resetFormQllhp();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm lớp học phần thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnThemLHPActionPerformed

