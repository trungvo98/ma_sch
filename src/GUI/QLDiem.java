    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        float diemqt = Float.parseFloat(txtdiemqt.getText());
        float diemgk = Float.parseFloat(txtdiemgk.getText());
        float diemck = Float.parseFloat(txtdiemck.getText());
        String masv = cbmasv.getSelectedItem().toString();
        String malhp = cblhp.getSelectedItem().toString();
        String hocky = MainGV.hocky;
        String namhoc = MainGV.namhoc;

        Diem d = new Diem(masv, malhp, hocky, namhoc, diemqt, diemgk, diemck);

        int check = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn lưu thay đổi ", "Thông báo ", JOptionPane.YES_NO_OPTION);
        if (check == JOptionPane.YES_OPTION) {
            boolean update = new DiemDAO().updateDiem(d);
            if (update) {
                JOptionPane.showMessageDialog(this, "Cập nhật điểm thành công ", "Thông báo ", JOptionPane.INFORMATION_MESSAGE);
                loadDataDiem();
            } else {
                JOptionPane.showMessageDialog(this, "Cập nhật thất bại ", "Thông báo ", JOptionPane.INFORMATION_MESSAGE);
            }

        }
    }//GEN-LAST:event_btnsaveActionPerformed

