    public void findSV() {
        listSV = new SinhVienDAO().findSVByName(txtfind.getText());
        int i = 1;
        model.setRowCount(0);
        for (SinhVien sv : listSV) {
            model.addRow(toObjectsDataSV(sv, i));
            i++;
        }
        bangdiemsv.setModel(model);

    }

    public void loadDataDiem() {
        model.setRowCount(0);
        listSV = new SinhVienDAO().findSVByLHP(cblhp.getSelectedItem().toString(), MainGV.hocky, MainGV.namhoc);
        int i = 1;
        for (SinhVien sv : listSV) {
            model.addRow(toObjectsDataSV(sv, i));
            i++;
        }
        bangdiemsv.setModel(model);
        setTongso();
    }

    public void setTongso() {

        txtsumrow.setText(Integer.toString(bangdiemsv.getRowCount()));
    }

    public void loadComboBox() {
        listMH = new MonHocDAO().findByMaGV_DK(Main.nameLogin2, MainGV.hocky, MainGV.namhoc);
        for (MonHoc mh : listMH) {
            dcbmMaMH.addElement(mh.getMaMH());
            dcbmTenMH.addElement(mh.getTenMH());

        }
        cbmamh.setModel(dcbmMaMH);
        cbtenmh.setModel(dcbmTenMH);

        listLHP = new LopHocPhanDAO().findByMaGV_DK(Main.nameLogin2, cbmamh.getSelectedItem().toString(), MainGV.hocky, MainGV.namhoc);
        dcbmLHP.removeAllElements();
        for (LopHocPhan lhp : listLHP) {
            dcbmLHP.addElement(lhp.getMaLHP());
        }
        cblhp.setModel(dcbmLHP);

    }

    public Object[] toObjectsDataSV(SinhVien sv, int i) {

        Diem diem = new DiemDAO().findDiemSV_LHP(sv.getMaSV(), cblhp.getSelectedItem().toString(), MainGV.hocky, MainGV.namhoc);
        Object[] ob = {i, sv.getMaSV(), sv.getHoTen(), diem.getDiemQT(), diem.getDiemGK(), diem.getDiemCK(), diem.getDiemTB()};
        return ob;
    }

