    private void btnXoaMHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaMHActionPerformed
        String mamh = txtmamh.getText();
        int check = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa môn học này.", "Thông báo", JOptionPane.YES_NO_OPTION);
        if (check == JOptionPane.YES_OPTION) {
            ArrayList<LopHocPhan> listLHP = new LopHocPhanDAO().deleteByMaMH(mamh);
            for (LopHocPhan lhp : listLHP) {
                new DangKyHocDAO().deleteByMaLHP(lhp.getMaLHP(), lhp.getHocky(), lhp.getNamhoc());
                Entities.LichThi lt = new Entities.LichThi(lhp.getMaLHP(), lhp.getHocky(), lhp.getNamhoc());
                new LichThiDAO().xoaLichthi(lt);
                new LopHocPhanDAO().xoaLHP(lhp);
            }
            boolean delete = new MonHocDAO().xoaMonhoc(mamh);
            if (delete) {
                JOptionPane.showMessageDialog(this, "Xóa môn học thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                loadQlmh();
                resetFormQlmh();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnXoaMHActionPerformed

