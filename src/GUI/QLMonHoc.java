    public void resetFormQlmh() {
        txtmamh.setText("");
        txttenmh.setText("");
        txtsotc.setText("");
        cbmakhoa.setSelectedIndex(0);
        cbmamhtr.setSelectedIndex(0);

        txtmamh.setEnabled(true);
        btnThemMH.setEnabled(true);
        btnSuaMH.setEnabled(false);
        btnXoaMH.setEnabled(false);
    }

    public void loadComboBox() {
        listMH = new MonHocDAO().getAll();
        dcbmMaMH.addElement("");
        dcbmTenMH.addElement("");
        for (MonHoc mh : listMH) {
            dcbmMaMH.addElement(mh.getMaMH());
            dcbmTenMH.addElement(mh.getTenMH());

        }
        cbmamhtr.setModel(dcbmMaMH);
        cbtenmhtr.setModel(dcbmTenMH);

        listKhoa = new KhoaDAO().getAll();

        for (Khoa khoa : listKhoa) {
            dcbmMakhoa2.addElement(khoa.getMakhoa());
        }
        cbmakhoa.setModel(dcbmMakhoa2);

    }

    public void loadQlmh() {
        model.setRowCount(0);

        listMH = new MonHocDAO().getAll();
        int i = 1;
        for (MonHoc mh : listMH) {
            model.addRow(toObjectsDataMH(mh, i));

            i++;
        }
        bangdsmh.setModel(model);

    }

    public String mhTruoc(MonHoc mh) {
        ArrayList<MonHoc> list = new MonHocDAO().findMaMH_TRUOC(mh.getMaMH());
        String strmh = "";
        for (int i = 0; i < list.size(); i++) {
            strmh = strmh + list.get(i).getMaMH();
        }
        return strmh;
    }

    public Object[] toObjectsDataMH(MonHoc mh, int i) {

        Object[] ob = {i, mh.getMaMH(), mh.getTenMH(), mh.getSoTC(), mhTruoc(mh), mh.getMaKhoa()};
        return ob;
    }

